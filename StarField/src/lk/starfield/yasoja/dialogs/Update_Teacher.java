    private void loadSubjects() {
        try {
            ResultSet resultSet = Mysql.search("SELECT * FROM `subject`");

            Vector v = new Vector();
            v.add("Select Subject");

            while (resultSet.next()) {
                v.add(resultSet.getString("name"));
                subjectMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            DefaultComboBoxModel m = new DefaultComboBoxModel(v);
            jComboBox1.setModel(m);

        } catch (SQLException e) {
            System.out.println(e);

        }
    }

    private void loadStatus() {
        try {
            ResultSet resultSet = Mysql.search("SELECT * FROM `status` LIMIT 2");

            Vector v = new Vector();
            v.add("Select Status");
            while (resultSet.next()) {
                v.add(resultSet.getString("status"));
            }
            DefaultComboBoxModel m = new DefaultComboBoxModel(v);
            jComboBox2.setModel(m);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

    private void setTeacherDetails() {

        roundTextField7.setText(Tno);

        try {
            ResultSet teacher = Mysql.search("SELECT * FROM `teacher`"
                    + "INNER JOIN `subject` ON `teacher`.`subject_id`=`subject`.`id` WHERE `teacher`.`id`='" + Tno + "'");
            if (teacher.next()) {
                roundTextField1.setText(teacher.getString("fname"));
                roundTextField2.setText(teacher.getString("lname"));
                roundTextField3.setText(teacher.getString("email"));
                roundTextField4.setText(teacher.getString("nic"));
                roundTextField5.setText(teacher.getString("mobile1"));
                roundTextField6.setText(teacher.getString("mobile2"));
                jComboBox1.setSelectedItem(teacher.getString("subject.name"));
                jComboBox2.setSelectedIndex(teacher.getInt("status_id"));
                roundDateChooser1.setDate(teacher.getDate("dob"));
                 jComboBox1.setEnabled(false);
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }

    }

