    private void roundButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton1ActionPerformed
        String fname = roundTextField1.getText();
        String lname = roundTextField2.getText();
        String email = roundTextField3.getText();
        String nic = roundTextField4.getText();
        String mobile1 = roundTextField5.getText();
        String mobile2Text = roundTextField6.getText();
        Date dob = roundDateChooser1.getDate();
        String subject = String.valueOf(jComboBox1.getSelectedItem());
        int status_ID = jComboBox2.getSelectedIndex();

        if (fname.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter First Name");
        } else if (lname.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Last Name");
        } else if (email.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Email");
        } else if (!email.matches(Validation.EMAIL_VALIDATION.validate())) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Invalid Email");
        } else if (nic.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter NIC");
        } else if (dob == null) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a Date Of Birth");
        } else if (!dob.before(new Date())) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a valid date");
        } else if (mobile1.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Mobile");
        } else if (!mobile1.matches(Validation.MOBILE_VALIDATION.validate())) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Invalid Mobile");
        } else if (subject.equals("Select Subject")) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a Batch ");
        } else if (status_ID == 0) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a Status");
        } else {
            String mobile2 = "";
            if (!mobile2Text.isBlank()) {
                if (!mobile2Text.matches(Validation.MOBILE_VALIDATION.validate())) {
                    Notifications.getInstance().show(Notifications.Type.WARNING,
                            Notifications.Location.TOP_CENTER, "Invalid Second Mobile");
                } else {
                    mobile2 = mobile2Text;
                }
            }
            String subjectID = subjectMap.get(subject);
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dobText = sdf.format(dob);

            Mysql.iud("UPDATE `teacher` SET `fname`='" + fname + "',`lname`='" + lname + "',`nic`='" + nic + "',`mobile1`='" + mobile1 + "',"
                    + "`mobile2`='" + mobile2 + "',`email`='" + email + "',`subject_id`='" + subjectID + "',"
                    + "`status_id`='" + status_ID + "',`dob`='" + dobText + "' WHERE `id`='" + Tno + "' ");

            Notifications.getInstance().show(Notifications.Type.SUCCESS,
                    Notifications.Location.TOP_LEFT, "Teacher Details Updated");

            roundTextField1.setText("");
            roundTextField2.setText("");
            roundTextField3.setText("");
            roundTextField4.setText("");
            roundTextField5.setText("");
            roundTextField6.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
            roundDateChooser1.setDate(null);
            this.dispose();
        }
    }//GEN-LAST:event_roundButton1ActionPerformed

