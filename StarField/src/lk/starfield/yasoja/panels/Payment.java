    private void roundButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton5ActionPerformed
        String STno = ID.getText();
        String Cno = classID.getText();

        try {
            ResultSet student = Mysql.search("SELECT * FROM `student` WHERE `id`='" + STno + "'");

            ResultSet class_rs = Mysql.search("SELECT * FROM `class`"
                    + " INNER JOIN `teacher` ON `class`.`teacher_id`=`teacher`.`id`"
                    + "INNER JOIN `subject` ON `teacher`.`subject_id`=`subject`.`id`"
                    + " WHERE `class`.`id`='" + Cno + "' ");

            if (STno.isBlank()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Please select a batch");
            } else if (!student.next()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Batch ID does not match any reistered Data");
            }
            if (Cno.isBlank()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Please select a class");
            } else if (!class_rs.next()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Class ID does not match any reistered Data");
            } else {

                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                Vector v = new Vector();
                v.add(class_rs.getString("subject.name"));
                v.add(class_rs.getString("teacher.fname") + " " + class_rs.getString("teacher.lname"));
                v.add(class_rs.getString("class.id"));
                v.add(class_rs.getString("start_time") + " " + class_rs.getString("end_time"));
                v.add(class_rs.getString("subject.fee"));

                model.addRow(v);
                jTable1.setModel(model);

                total = total + class_rs.getDouble("subject.fee");
                totalLabel.setText(String.valueOf(total));
                classID.setText("");
                classFees.setText("");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_roundButton5ActionPerformed

