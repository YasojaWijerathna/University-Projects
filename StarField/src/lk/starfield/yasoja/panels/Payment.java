    private void roundButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton6ActionPerformed

        String STno = ID.getText();
        String name = Name.getText();
        String batch = Batch.getText();
        String nic = NIC.getText();
        String email = Email.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        String date = sdf.format(new java.util.Date());
        String invoice = String.valueOf(System.currentTimeMillis());

        int rowcount = jTable1.getRowCount();
        if (STno.isBlank() || name.isBlank() || batch.isBlank() || nic.isBlank() || email.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a student");
        } else if (rowcount > 0) {

            try {
                HashMap<String, Object> parameters = new HashMap<>();
                parameters.put("invoice", invoice);
                parameters.put("date", date);
                parameters.put("name", name);
                parameters.put("batch", batch);
                parameters.put("nic", nic);
                parameters.put("email", email);
                parameters.put("total", String.valueOf(total));
                
                JasperReport compiledReport = JasperCompileManager.compileReport("src/lk/starfield/yasoja/reports/Starfield_invoice.jrxml");
                JRDataSource datasource = new JRTableModelDataSource(jTable1.getModel());
                JasperPrint report = JasperFillManager.fillReport(compiledReport, parameters, datasource);
                JasperViewer.viewReport(report,false);
                
                for (int x=0;x< rowcount;x++){
                    String Cno=String.valueOf(jTable1.getValueAt(x,2));
                    
                    Mysql.iud("UPDATE `class_enroll` SET `payment_status`='5' "
                            + "WHERE `student_ID`='"+STno+"' AND `class_id`='"+Cno+"' ");
                
                    Mysql.iud("INSERT INTO `invoice` VALUES ('"+invoice+"','"+date+"','"+Cno+"','"+STno+"') ");
                }
            } catch (Exception e) {

                e.printStackTrace();
            }
        } else {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select and a class to the table");
        }
    }//GEN-LAST:event_roundButton6ActionPerformed

    private void classIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_classIDKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String class_id = classID.getText();

            try {
                ResultSet rs = Mysql.search("SELECT * FROM `class`"
                        + "INNER JOIN `teacher` ON `class`.`teacher_id`=`teacher`.`id`"
                        + "INNER JOIN `subject` ON `teacher`.`subject_id`=`subject`.`id`"
                        + " WHERE `class`.`id` = '" + class_id + "' ");

                if (rs.next()) {
                    classID.setText(rs.getString("class.id"));
                    classFees.setText(rs.getString("subject.fee"));
                } else {
                    classID.setText("");
                }
            } catch (Exception e) {
                throw new RuntimeException(e);
            }
        }
    }//GEN-LAST:event_classIDKeyReleased


