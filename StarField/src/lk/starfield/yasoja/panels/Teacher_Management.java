    private void roundButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton5ActionPerformed
        String Tno = ID.getText();
        String Cno = classID.getText();

        try {
            ResultSet teacher = Mysql.search("SELECT * FROM `teacher` WHERE `teacher`.`id`='" + Tno + "'");

            ResultSet class_rs = Mysql.search("SELECT * FROM `class` WHERE `id`='" + Cno + "' ");

            if (Tno.isBlank()) {

                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Please select a teacher");
            } else if (!teacher.next()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Teacher ID does not match any reistered Data");
            }else if (Cno.isBlank()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Please select a class");
            } else if (!class_rs.next()) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Class ID does not match any reistered Data");
            } else {

                String assignedTeacher = class_rs.getString("teacher_id");
                if (assignedTeacher.equals("T_000")) {
                    Mysql.iud("UPDATE `class` SET `teacher_id`='" + Tno + "' `status_id`='8' WHERE `id`='" + Cno + "' ");

                    Notifications.getInstance().show(Notifications.Type.SUCCESS,
                            Notifications.Location.TOP_CENTER, "Teacher assigned to class successfully");

                    loadTable(Tno);
                    classID.setText("");
                } else {
                    Notifications.getInstance().show(Notifications.Type.WARNING,
                            Notifications.Location.TOP_CENTER, "A teacher is already assigned to select class");
                }
            }
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }//GEN-LAST:event_roundButton5ActionPerformed

