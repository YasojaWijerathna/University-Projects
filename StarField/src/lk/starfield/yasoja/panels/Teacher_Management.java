    private void roundButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton4ActionPerformed
        String Tno = ID.getText();

        if (Tno.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please type Teacher No to search for teacher");
        } else {

            try {
                ResultSet teacher_details = Mysql.search("SELECT * FROM `teacher`"
                        + "INNER JOIN `subject` ON `teacher`.`subject_id`=`subject`.`id` WHERE `teacher`.`id`='" + Tno + "' ");

                if (teacher_details.next()) {
                    ID.setText(teacher_details.getString("teacher.id"));
                    Name.setText(teacher_details.getString("fname") + " " + teacher_details.getString("lname"));
                    Batch.setText(teacher_details.getString("name"));
                    NIC.setText(teacher_details.getString("nic"));
                    Email.setText(teacher_details.getString("email"));
                    loadTable(Tno);

                } else {
                    Notifications.getInstance().show(Notifications.Type.WARNING,
                            Notifications.Location.TOP_CENTER, "Teacher ID does not match any reistered Data");
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_roundButton4ActionPerformed

    public void loadTable(String Tno) {
        try {
            ResultSet teacher_class = Mysql.search("SELECT * FROM `teacher`"
                    + "INNER JOIN `class` ON `class`.`teacher_id`=`teacher`.`id`"
                    + "INNER JOIN `class_day` ON `class`.`class_day_id`=`class_day`.`id`"
                    + "INNER JOIN `subject` ON `teacher`.`subject_id`=`subject`.`id`"
                    + "WHERE `teacher`.`id` = '" + Tno + "' ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (teacher_class.next()) {

                Vector v = new Vector();
                v.add(teacher_class.getString("class.id"));
                v.add(teacher_class.getString("day"));
                v.add(teacher_class.getString("start_time") + "-" + teacher_class.getString("end_time"));

                String classStatus = "";
                if (teacher_class.getInt("class.status_id") == 3) {
                    classStatus = Valid_Status.ONGOING.name();
                } else if (teacher_class.getInt("class.status_id") == 4) {
                    classStatus = Valid_Status.COMPLETED.name();
                }
                v.add(classStatus);

                model.addRow(v);
                jTable1.setModel(model);
            }
        } catch (Exception e) {

        }
    }

