    private void roundButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton2ActionPerformed

        String name = roundTextField2.getText();
        String mobile = roundTextField3.getText();

        if (name.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Comoany Name");

        }
        if (mobile.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Mobile");

        } else if (!mobile.matches(Validation.MOBILE_VALIDATION.validate())) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Invalid Mobile");
        } else {
            Mysql.iud("UPDATE `company` SET `name`='" + name + "',`mobile`='" + mobile + "' WHERE `id`='"+companyID+"' ");

            Notifications.getInstance().show(Notifications.Type.SUCCESS,
                    Notifications.Location.TOP_CENTER, "Company Details Successfully Updated");

            roundTextField2.setText("");
            roundTextField3.setText("");
            this.dispose();
        }
    }//GEN-LAST:event_roundButton2ActionPerformed


