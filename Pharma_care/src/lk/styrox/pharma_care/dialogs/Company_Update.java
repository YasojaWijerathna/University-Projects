    private void loadSupplier() {
        idField.setText(companyID);
        try {
            ResultSet rs = Mysql.search("SELECT * FROM `company`"
                    + "WHERE `id`='" + companyID + "' ");

            if (rs.next()) {
                roundTextField2.setText(rs.getString("name"));
                roundTextField3.setText(rs.getString("mobile"));

            }

        } catch (Exception e) {
            e.printStackTrace();
            Logger.AdminlLog(String.valueOf(e));
        }
    }

