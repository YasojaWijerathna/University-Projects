    private void roundButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton1ActionPerformed
        String stock_id = IDField.getText();
        String product_name = PNameField.getText();
        String category_name = categoryField.getText();
        String brand_name = brandField.getText();

        String query = "SELECT * FROM `stock`"
                + "INNER JOIN `product` ON `stock`.`product_id`=`product`.`id`"
                + "INNER JOIN `category` ON `product`.`category_id`=`category`.`id`"
                + "INNER JOIN `brand` ON `product`.`brand_id`=`brand`.`id`"
                + "WHERE `status_id`='1'";

        if (stock_id.isBlank()) {

            if (product_name.isBlank() && category_name.isBlank() && brand_name.isBlank()) {
                Notifications
                        .getInstance().show(Notifications.Type.WARNING,
                                Notifications.Location.TOP_CENTER, "Please enter Search details");
                loadTable();
            } else if (!product_name.isBlank() && category_name.isBlank() && brand_name.isBlank()) {
                query = query + " AND `product`.`name` LIKE '%" + product_name + "%'";
            } else if (!product_name.isBlank() && !category_name.isBlank() && brand_name.isBlank()) {
                query = query + " AND `category`.`name` LIKE '%" + category_name + "%'";
            } else if (product_name.isBlank() && category_name.isBlank() && !brand_name.isBlank()) {
                query = query + " AND `brand`.`name` LIKE '%" + brand_name + "%' ";
            } else {
                loadTable();
            }
        } else {
            query = query + " WHERE `product`.`id` LIKE'%" + stock_id + "%'";
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = Mysql.search(query);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("stock.barcode"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("category.name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("stock.qty"));
                v.add(rs.getString("stock.selling_price"));
                v.add(rs.getString("stock.mfd"));
                v.add(rs.getString("stock.exp"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
           Logger.CashierLog(String.valueOf(e));
            e.printStackTrace();
        }
    }//GEN-LAST:event_roundButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectRow=jTable1.getSelectedRow();
        
        if (evt.getClickCount() == 2) {
            String barcode = String.valueOf(jTable1.getValueAt(selectRow, 0));
            String productName = String.valueOf(jTable1.getValueAt(selectRow, 1));
            String Category = String.valueOf(jTable1.getValueAt(selectRow, 2));
            String Brand = String.valueOf(jTable1.getValueAt(selectRow, 3));
            String Qty = String.valueOf(jTable1.getValueAt(selectRow, 4));
            String Price = String.valueOf(jTable1.getValueAt(selectRow, 5));

            invoice_panel.getPIDfield().setText(barcode);
            invoice_panel.getPNameField().setText(productName);
            invoice_panel.getCategoryField().setText(Category);
            invoice_panel.getBrandField().setText(Brand);
            invoice_panel.getPriceField().setText(Price);
           invoice_panel.maxQty=Integer.parseInt(Qty);
            
            this.dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

