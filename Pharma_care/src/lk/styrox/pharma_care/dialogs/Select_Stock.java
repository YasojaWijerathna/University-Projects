    private void loadTable() {
        TableCustom.apply(tableScrollPane, TableCustom.TableType.DEFAULT);

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = Mysql.search("SELECT * FROM `stock`"
                    + "INNER JOIN `product` ON `stock`.`product_id`=`product`.`id`"
                    + "INNER JOIN `category` ON `product`.`category_id`=`category`.`id`"
                    + "INNER JOIN `brand` ON `product`.`brand_id`=`brand`.`id`"
                    + "WHERE `status_id`='1'");

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("stock.barcode"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("category.name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("stock.qty"));
                v.add(rs.getString("stock.selling_price"));
                v.add(rs.getString("stock.mfd"));
                v.add(rs.getString("stock.exp"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
           Logger.CashierLog(String.valueOf(e));
            e.printStackTrace();
        }

    }

