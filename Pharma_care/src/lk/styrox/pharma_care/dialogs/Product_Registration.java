    private void roundButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton2ActionPerformed

        String name = roundTextField2.getText();
        String category = String.valueOf(jComboBox1.getSelectedItem());
        String brand = String.valueOf(jComboBox2.getSelectedItem());

        if (name.isBlank()) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Search details");

        } else if (category.equals("Select Category")) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a category");

        } else if (brand.equals("Select Brand")) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please select a brand ");

        } else {
            String brandID = brandMap.get(brand);
            String categoryID = categoryMap.get(category);
            
            String productID=getID();

            Mysql.iud("INSERT INTO `product` (`id`,`name`,`category_id`,`brand_id`)"
                    + " VALUE ('P_"+productID+"','" + name + "','"+categoryID+"','"+brandID+"')");

            Notifications.getInstance().show(Notifications.Type.SUCCESS,
                    Notifications.Location.TOP_CENTER, "Product Successfully Registered");

            roundTextField2.setText("");
            this.dispose();
        }
    }//GEN-LAST:event_roundButton2ActionPerformed

