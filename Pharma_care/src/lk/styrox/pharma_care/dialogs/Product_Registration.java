    private String getID() {
        String newID = "";
        try {
            ResultSet rs = Mysql.search("SELECT * FROM `product` ORDER BY `id` DESC LIMIT 1 ");

            if (rs.next()) {
                String lastID = rs.getString("id");

                String[] x = lastID.split("_");
                int idNo = Integer.parseInt(x[1]);
                newID = String.format("%03d", idNo + 1);
            } else {
                newID = "001";
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return newID;
    }

