    private void loadCompany() {
        try {
            ResultSet resultSet = Mysql.search("SELECT * FROM `company` ");

            Vector v = new Vector();
            v.add("Select Company");

            while (resultSet.next()) {
                v.add(resultSet.getString("name"));
                companyMap.put(resultSet.getString("name"), resultSet.getString("id"));

            }
            DefaultComboBoxModel m = new DefaultComboBoxModel(v);
            jComboBox1.setModel(m);

        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void loadTable() {
        TableCustom.apply(tableScrollPane, TableCustom.TableType.DEFAULT);

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = Mysql.search("SELECT * FROM `supplier`"
                    + "INNER JOIN `company` ON `supplier`.`company_id`=`company`.`id`"
                    + "INNER JOIN `user` ON `supplier`.`user_id`=`user`.`id`"
                    + "WHERE `supplier`.`status_id`='1'  ");

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("supplier.id"));
                v.add(rs.getString("user.fname") + " " + rs.getString("user.lname"));
                v.add(rs.getString("company.name"));
                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            Logger.AdminlLog(String.valueOf(e));
            e.printStackTrace();
        }

    }

