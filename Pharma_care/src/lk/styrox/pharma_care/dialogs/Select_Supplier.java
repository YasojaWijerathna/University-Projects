    private void roundButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton1ActionPerformed
        String supplier_id = roundTextField1.getText();
        String supplier_name = roundTextField2.getText();
        String company_name = String.valueOf(jComboBox1.getSelectedItem());

        String query = "SELECT * FROM `supplier`"
                    + "INNER JOIN `company` ON `supplier`.`company_id`=`company`.`id`"
                    + "INNER JOIN `user` ON `supplier`.`user_id`=`user`.`id`"
                    + "WHERE `supplier`.`status_id`='1'";

        if (supplier_id.isBlank()) {

            if (supplier_name.isBlank() && company_name.equals("Select Company")) {
                Notifications.getInstance().show(Notifications.Type.WARNING,
                        Notifications.Location.TOP_CENTER, "Please enter Search details");
                loadTable();
            } else if (!supplier_name.isBlank() && company_name.equals("Select Company")) {
                query = query + " AND (`user`.`fname` LIKE '%" + supplier_name + "%' "
                    + " OR `user`.`lname` LIKE '%" + supplier_name + "%')";
                
            } else if (supplier_name.isBlank() && !company_name.equals("Select Company")) {
                String companyID = companyMap.get(company_name);

                query = query + " AND `supplier`.`company_id` = '" + companyID + "'";
            } else {
                loadTable();
            }
        } else {
            query = query + " AND `supplier`.`id` LIKE'%" + supplier_id + "%'";
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = Mysql.search(query);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("supplier.id"));
                v.add(rs.getString("user.fname") + " " + rs.getString("user.lname"));
                v.add(rs.getString("company.name"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (Exception e) {
            Logger.AdminlLog(String.valueOf(e));
            e.printStackTrace();
        }
    }//GEN-LAST:event_roundButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectRow = jTable1.getSelectedRow();

        if (evt.getClickCount() == 2) {
            String supplierID = String.valueOf(jTable1.getValueAt(selectRow, 0));
            String supplierName = String.valueOf(jTable1.getValueAt(selectRow, 1));
            String Company = String.valueOf(jTable1.getValueAt(selectRow, 2));
 

            grn_panel.getSPIDfield().setText(supplierID);
            grn_panel.getSupplierField().setText(supplierName);
            grn_panel.getCompanyField().setText(Company);
          

            this.dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked
