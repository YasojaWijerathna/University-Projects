    public void loadTable() {
        TableCustom.apply(tableScrollPane, TableCustom.TableType.DEFAULT);
        JTableHeader header = jTable1.getTableHeader();
        header.setFont(new Font("San-Serif", Font.BOLD, 13));

        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet rs = Mysql.search("SELECT * FROM `invoice`"
                    + "INNER JOIN `invoice_item` ON `invoice`.`id`=`invoice_item`.`invoice_id`"
                    + "INNER JOIN `stock` ON `stock`.`barcode`=`invoice_item`.`barcode`"
                    + "INNER JOIN `product` ON `product`.`id`=`stock`.`product_id`"
                    + "INNER JOIN `brand` ON `brand`.`id`=`product`.`brand_id`"
                    + "ORDER BY `invoice`.`id`");

            while (rs.next()) {
                Vector<String> v = new Vector<>();
                v.add(rs.getString("invoice.id"));
                v.add(rs.getString("stock.barcode"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("invoice_item.qty"));
                v.add(rs.getString("stock.selling_price"));
                v.add(rs.getString("invoice.issue_date"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (SQLException e) {
            Logger.CashierLog(String.valueOf(e));
            e.printStackTrace();
        }
    }

