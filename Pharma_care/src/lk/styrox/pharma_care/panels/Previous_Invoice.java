    private void roundButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton1ActionPerformed
        String invoice_id = roundTextField1.getText();
        Date date = roundDateChooser1.getDate();
        
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");

        String query = "SELECT * FROM `invoice`"
                + "INNER JOIN `invoice_item` ON `invoice`.`id`=`invoice_item`.`invoice_id`"
                + "INNER JOIN `stock` ON `stock`.`barcode`=`invoice_item`.`barcode`"
                + "INNER JOIN `product` ON `product`.`id`=`stock`.`product_id`"
                + "INNER JOIN `brand` ON `brand`.`id`=`product`.`brand_id`";

        if (invoice_id.isBlank() && date==null) {
            Notifications.getInstance().show(Notifications.Type.WARNING,
                    Notifications.Location.TOP_CENTER, "Please enter Search details");

        } else if (invoice_id.isBlank() && date !=null) {
            query = query + " WHERE `invoice`.`issue_date`= '" + sdf.format(date) + "'";
        } else if (!invoice_id.isBlank() && date==null) {
            query = query + " WHERE `invoice`.`id` LIKE '%" + invoice_id + "%'";
        } else {
            loadTable();
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = Mysql.search(query);

            while (rs.next()) {

                Vector<String> v = new Vector<>();
               v.add(rs.getString("invoice.id"));
                v.add(rs.getString("stock.barcode"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("invoice_item.qty"));
                v.add(rs.getString("stock.selling_price"));
                v.add(rs.getString("invoice.issue_date"));

                model.addRow(v);
                jTable1.setModel(model);
            }

        } catch (SQLException e) {
            Logger.CashierLog(String.valueOf(e));
            e.printStackTrace();
        }
    }//GEN-LAST:event_roundButton1ActionPerformed

